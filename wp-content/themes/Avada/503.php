	<h2>New Site Will Be Launching Soon</h2>
	
	<p>Harpswell Coastal Academy (HCA) is a public charter school offering rigorous, personalized, project-based education to midcoast Maine students in grades 6–12. It plans to open its doors in September 2013.</p>

	<p>Originally initiated by a group of Harpswell citizens concerned that many of the Town’s young people were increasingly disengaged from school, HCA has grown to include a diverse group of mid- coast educators, business leaders, and community partners committed to preparing students to live and work in the rapidly changing coastal economy.</p>
	
	<p>Maine’s recently enacted public charter school legislation (joining 40 other states) enables HCA to provide a public school alternative delivering experiential learning while fully meeting the Maine core curriculum requirements.</p>
	
